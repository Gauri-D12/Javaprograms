package SolvedAssignments;

public class CircumRect 
{
  public static void main(String[] args)
    
    {
        double length = 5.0; 
        double width = 3.0; 
        double circumference = 2.0 * (length + width);

        System.out.println("The circumference of the rectangle with length " + length + " and width " + width + " is: " + circumference);
    }
}