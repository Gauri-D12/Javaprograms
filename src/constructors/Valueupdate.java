package constructors;

public class Valueupdate {

    public static void main(String[] args) {
        // Updating values for each primitive data type

        // Integer
        int intValue = 88;
        System.out.println("Original int value: " + intValue);
        intValue = 4; // Update value
        System.out.println("Updated int value: " + intValue);

        // Double
        double doubleValue = 88.5;
        System.out.println("Original double value: " + doubleValue);
        doubleValue = 2.5; // Update value
        System.out.println("Updated double value: " + doubleValue);

        // Float
        float floatValue = 11.5f;
        System.out.println("Original float value: " + floatValue);
        floatValue = 2.5f; // Update value
        System.out.println("Updated float value: " + floatValue);

        // Character
        char charValue = 'G';
        System.out.println("Original char value: " + charValue);
        charValue = 'S'; // Update value
        System.out.println("Updated char value: " + charValue);

        // Boolean
        boolean boolValue = true;
        System.out.println("Original boolean value: " + boolValue);
        boolValue = false; // Update value
        System.out.println("Updated boolean value: " + boolValue);

        // Long
        long longValue = 5000000000L;
        System.out.println("Original long value: " + longValue);
        longValue = 1000000000L; // Update value
        System.out.println("Updated long value: " + longValue);

        // Short
        short shortValue = 1500;
        System.out.println("Original short value: " + shortValue);
        shortValue = 600; // Update value
        System.out.println("Updated short value: " + shortValue);

        // Byte
        byte byteValue = 100;
        System.out.println("Original byte value: " + byteValue);
        byteValue = 50; // Update value
        System.out.println("Updated byte value: " + byteValue);
    }
}